        <h2><a name="FAQ8">How do I run your programs with SCORM?</a>
           <p>Simon Mellor-Clark, 6 May 2010</p>
        </h2>
     

        <p>You have to have a learner-tracking licence to integrate with SCORM since the learner details are passed to the Clarity program (so it is not anonymous) and then the Clarity program sends progress data back to Moodle's built-in reports (so it is tracking). SCORM handles all login, so if a student has a Moodle account and is enrolled in a course that contains a unit from a Clarity program, they will automatically be able to run that unit. If it is their first time to do so, the Clarity program will add their details to the database and check off one licence. The same details will be picked up when the same  student runs that program (from<strong> </strong>any  Moodle course).</p>
<p>The first step is just to check that you are satisfied with the Moodle integration, so if you visit <a href="http://www.clarityenglish.com/support/tech/tech04_integration/tech0402.php" target="_blank">here</a> and download one of the learning objects. You should then be able to import this as a SCORM object into a Moodle course. The actual software and content are all hosted on Clarity servers, with  pointers going into your system. This keeps everything very small and tidy.</p>
		
      <p class="top"><a href="#top">Top</a></p>

        
