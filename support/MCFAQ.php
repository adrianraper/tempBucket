     	<h2><a name="FAQ12">I removed the checkmarks beside the exercise titles in Results Manager to disable exercises for selected students, but I found they are enabled again afterwards when I log in as administrator to change students' profiles. What did I do?</a> 
     	  <p>Aldo, 28 May 2010</p></h2>
       
        <p>In Results Manager, the function to disable exercises for students is called &quot;hidden content".
Hidden Content is group-specific. When teachers log into Results Manager, they only see their own group(s) of students and change the content the students can access. So two different teachers can set up profiles of content that are very different for their students without any clash.
Administrators can see all groups in their institution.</p>
<p>My guess is that Aldo clicked on two different  groups when he logged in as a teacher and later as an administrator. If you click on a student (or indeed a teacher) on the right of the screen, rather than a group, the checkmarks all disappear because you can't set hidden content for a single student, only for a whole group.</p>
     	<p class="top"><a href="#top">Top</a></p>

